import UIKit

extension UIColor {
    
    static func alarmYellow () -> UIColor {
       return UIColor(red: 1.000, green: 1.000, blue: 0.039, alpha: 1.00)
    }
    
    static func alarmBlue() -> UIColor {
       return UIColor(red: 0.047, green: 0.361, blue: 0.769, alpha: 1.00)
    }
    
    static func alarmPink() -> UIColor {
        return UIColor(red: 0.988, green: 0.278, blue: 0.761, alpha: 1.00)
    }
    
    
    
    
    
}


